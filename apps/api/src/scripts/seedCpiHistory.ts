export function seedCpiHistory(): number {
  const db = getDb();

  const stmt = db.prepare(
    `INSERT INTO cpi_tr (date, yoy_change, mom_change, source, updated_at)
     VALUES (?, ?, ?, 'tuik.gov.tr', ?)
     ON CONFLICT(date) DO UPDATE SET
       yoy_change = excluded.yoy_change,
       mom_change = excluded.mom_change,
       source = excluded.source,
       updated_at = excluded.updated_at`,
  );

  let count = 0;
  const txn = db.transaction(() => {
    for (const [period, yoy, mom] of CPI_DATA) {
      const date = `${period}-01`;
      stmt.run(date, yoy / 100, mom / 100, Date.now());
      count++;
    }
  });
  txn();

  const total = (db.prepare('SELECT COUNT(*) as c FROM cpi_tr').get() as { c: number }).c;
  console.log(`[cpi-seed] ${count} aylık TÜFE verisi yazıldı (2016–2025)`);
  console.log(`[cpi-seed] Toplam cpi_tr kayıt: ${total}`);

  return count;
}

if (import.meta.url === `file://${process.argv[1]}`) {
  try {
    seedCpiHistory();
    process.exit(0);
  } catch (err) {
    console.error('[cpi-seed] hata:', err);
    process.exit(1);
  }
}
